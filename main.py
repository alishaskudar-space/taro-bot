def md_escape(s: str) -> str:
    # –¥–ª—è parse_mode="Markdown"
    return (
        s.replace("\\", "\\\\")
        .replace("*", "\\*")
        .replace("_", "\\_")
        .replace("`", "\\`")
        .replace("[", "\\[")
    )


def draw_unique_cards(n: int):
    # –±–µ–∑–æ–ø–∞—Å–Ω–æ: –µ—Å–ª–∏ –∫–æ–ª–æ–¥–∞ –º–∞–ª–µ–Ω—å–∫–∞—è ‚Äî –Ω–µ –∑–∞–≤–∏—Å–∞–µ–º
    drawn = []
    seen = set()
    attempts = 0
    while len(drawn) < n and attempts < 500:
        code, orient, path = get_random_card()
        attempts += 1
        if code in seen:
            continue
        seen.add(code)
        drawn.append((code, orient, path))
    # –µ—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å –¥–æ–±—Ä–∞—Ç—å —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ ‚Äî –¥–æ–±–∏–≤–∞–µ–º –ª—é–±—ã–º–∏
    while len(drawn) < n:
        drawn.append(get_random_card())
    return drawn


